public class LoopsDecrements {


    public static void main(String[] args) {
        String[] names = new String[]{"Zygfryd", "Gwidon", "Florentyna", "a", "b", "c", "d", "e",};
        for (int i = names.length -1; i >=0; i--) {
            System.out.println(names[i]);
        }

    }
}















